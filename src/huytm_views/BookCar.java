    private void btnCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckActionPerformed
        // TODO add your handling code here:
        int search = 0;
        switch (cbSeat.getSelectedIndex()) {
            case 1:
                search = 2;
                break;
            case 2:
                search = 4;
                break;
            case 3:
                search = 7;
                break;
            case 4:
                search = 16;
                break;
            case 5:
                search = 45;
                break;
        }
        CarDAO dao = new CarDAO();
        List<CarDTO> result;
        try {
            result = dao.listCarBySeat(search);
            showCar(result);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }

    }//GEN-LAST:event_btnCheckActionPerformed

    private void tblViewMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblViewMouseClicked
        // TODO add your handling code here:
        int row = tblView.getSelectedRow();
        String carID = (String) tblModelCar.getValueAt(row, 0);

        //
        CarDAO dao = new CarDAO();
        try {
            CarDTO dto = dao.viewDetailCar(carID);

            if (!dto.getImageLink().equals("")) {
                try {
                    currentImage = ImageIO.read(new File(dto.getImageLink()));
                    showImage();
                } catch (Exception e) {
                    currentImage = null;
                    JOptionPane.showMessageDialog(this, e);
                }
            } else {
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            ex.printStackTrace();
        }


    }//GEN-LAST:event_tblViewMouseClicked

