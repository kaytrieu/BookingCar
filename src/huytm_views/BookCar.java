    private void showCar(List<CarDTO> list) {
        tblModelCar.setRowCount(0);
        for (CarDTO carDTO : list) {
            tblModelCar.addRow(carDTO.toVector());
        }

    }

    private void showImage() {
        if (currentImage != null) {
            g.clearRect(x, y, pImage.getWidth(), pImage.getHeight());
            int imgWidth = currentImage.getWidth();
            int imgHeight = currentImage.getHeight();
            double ratio = 1.0 * imgWidth / imgHeight;
            int areaWidth = this.pImage.getWidth() - (2 * x);
            int areaHeight = this.pImage.getHeight() - (2 * y);
            int newWidth = imgWidth;
            int newHeight = imgHeight;
            if (imgWidth > areaWidth) {
                newWidth = areaWidth;
                newHeight = (int) (newWidth / ratio);
            }
            if (newHeight > areaHeight) {
                newHeight = areaHeight;
                newWidth = (int) (newHeight * ratio);
            }
            Image scaledImage = currentImage.getScaledInstance(newWidth, newHeight, Image.SCALE_DEFAULT);
            g.drawImage(scaledImage, x, y, newWidth, newHeight, this.pImage);
        }
    }

